function generatePortrait(hH, hM, hD, fH, fM, fD, oH, oM, oD, accent) {
  // Generate a 64x64 portrait with detailed features
  const rows = [];
  for (let y = 0; y < 64; y++) {
    const pixels = [];
    const cx = 32; // center x

    if (y < 4) {
      // Empty top
    } else if (y < 8) {
      // Hair top
      const w = 12 + (y - 4);
      for (let x = cx - w; x <= cx + w; x++) pixels.push([x, hM]);
      // Highlights
      if (y === 5) { pixels.push([cx-4, hH]); pixels.push([cx+3, hH]); }
    } else if (y < 14) {
      // Hair + face area
      const faceL = cx - 10;
      const faceR = cx + 10;
      for (let x = faceL - 3; x <= faceR + 3; x++) {
        if (x < faceL || x > faceR) pixels.push([x, hM]);
        else if (x === faceL || x === faceR) pixels.push([x, hD]);
      }
      // Face
      for (let x = faceL + 1; x < faceR; x++) pixels.push([x, fH]);
      // Hair highlights
      if (y === 9) { pixels.push([faceL-1, hH]); pixels.push([faceR+1, hH]); }
    } else if (y < 20) {
      // Eyes area
      const faceL = cx - 10;
      const faceR = cx + 10;
      for (let x = faceL - 3; x <= faceR + 3; x++) {
        if (x < faceL || x > faceR) pixels.push([x, hM]);
        else if (x === faceL || x === faceR) pixels.push([x, hD]);
      }
      for (let x = faceL + 1; x < faceR; x++) pixels.push([x, fH]);
      // Eyes
      if (y === 15) {
        pixels.push([cx-5, 'K']); pixels.push([cx-4, 'K']); pixels.push([cx-3, 'K']);
        pixels.push([cx+3, 'K']); pixels.push([cx+4, 'K']); pixels.push([cx+5, 'K']);
      }
      if (y === 16) {
        pixels.push([cx-5, 'K']); pixels.push([cx-4, fH]); pixels.push([cx-3, 'K']);
        pixels.push([cx+3, 'K']); pixels.push([cx+4, fH]); pixels.push([cx+5, 'K']);
      }
    } else if (y < 24) {
      // Nose/mouth
      const faceL = cx - 10;
      const faceR = cx + 10;
      for (let x = faceL - 3; x <= faceR + 3; x++) {
        if (x < faceL || x > faceR) pixels.push([x, hM]);
        else if (x === faceL || x === faceR) pixels.push([x, fD]);
      }
      for (let x = faceL + 1; x < faceR; x++) pixels.push([x, fM]);
      if (y === 21) { pixels.push([cx, fD]); }
      if (y === 23) { pixels.push([cx-2, 'Z']); pixels.push([cx-1, 'Z']); pixels.push([cx, 'Z']); pixels.push([cx+1, 'Z']); pixels.push([cx+2, 'Z']); }
    } else if (y < 28) {
      // Neck/chin
      const w = 8 - (y - 24);
      for (let x = cx - w; x <= cx + w; x++) pixels.push([x, fM]);
    } else if (y < 34) {
      // Shoulders
      const w = 14 + (y - 28);
      for (let x = cx - w; x <= cx + w; x++) pixels.push([x, oM]);
      // Collar detail
      pixels.push([cx-2, accent]); pixels.push([cx-1, accent]);
      pixels.push([cx, accent]); pixels.push([cx+1, accent]); pixels.push([cx+2, accent]);
    } else if (y < 48) {
      // Body
      const w = 18 + Math.min(y - 34, 4);
      for (let x = cx - w; x <= cx + w; x++) pixels.push([x, oM]);
      // Shading
      for (let x = cx - w; x < cx - w + 3; x++) pixels.push([x, oD]);
      for (let x = cx + w - 2; x <= cx + w; x++) pixels.push([x, oH]);
      // Belt
      if (y === 40) {
        for (let x = cx - 12; x <= cx + 12; x++) pixels.push([x, 'Y']);
        pixels.push([cx, 'G']);
      }
    } else {
      // Lower body
      const w = 20 - (y - 48);
      if (w > 0) {
        for (let x = cx - w; x <= cx + w; x++) pixels.push([x, oD]);
      }
    }

    rows.push(row(pixels, 64));
  }
  return rows;
}

