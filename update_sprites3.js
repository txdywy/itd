// Helper: generate a row string from a sparse pixel map
function row(pixels, width = 32) {
  const arr = new Array(width).fill('.');
  for (const [x, color] of pixels) {
    if (x >= 0 && x < width) arr[x] = color;
  }
  return arr.join('');
}

// Helper: fill a range of columns
function fillRow(startX, endX, color) {
  const pixels = [];
  for (let x = startX; x <= endX; x++) pixels.push([x, color]);
  return pixels;
}

// Helper: merge multiple pixel arrays
function merge(...pixelArrays) {
  const result = [];
  for (const pa of pixelArrays) result.push(...pa);
  return result;
}

