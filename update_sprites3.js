function generateCode() {
  let code = '';

  // Palette
  code += PALETTE_CODE + '\n\n';

  // Portraits
  code += '// 头像数据 64x64\nconst PORTRAITS = {\n';
  for (const [name, rows] of Object.entries(PORTRAITS_DATA)) {
    code += `  "${name}": [\n`;
    for (const r of rows) {
      code += `    "${r}",\n`;
    }
    code += '  ],\n';
  }
  code += '};\n\n';

  // Sprites (new format: array of frame arrays)
  code += '// 角色精灵 32x48 (多帧动画)\nconst SPRITES = {\n';
  for (const [name, char] of Object.entries(CHARACTERS)) {
    code += `  "${name}": [\n`;
    for (const frame of char.frames) {
      code += '    [\n';
      for (const r of frame) {
        code += `      "${r}",\n`;
      }
      code += '    ],\n';
    }
    code += '  ],\n';
  }
  // Chest and cursor
  code += '  "chest": [\n';
  for (const frame of CHEST_SPRITE.frames) {
    code += '    [\n';
    for (const r of frame) {
      code += `      "${r}",\n`;
    }
    code += '    ],\n';
  }
  code += '  ],\n';
  code += '  "cursor": [\n';
  for (const frame of CURSOR_SPRITE.frames) {
    code += '    [\n';
    for (const r of frame) {
      code += `      "${r}",\n`;
    }
    code += '    ],\n';
  }
  code += '  ],\n';
  code += '};\n\n';

  return code;
}

